<h2 id="how-to-use">How To Use</h2>
<p>
  To clone and run this application, you’ll need
  <a href="https://git-scm.com" target="__blank">Git</a>
  and
  <a href="https://nodejs.org/en/download/" target="__blank">Node.js</a>
  (which comes with <a href="http://npmjs.com" target="__blank">npm</a>)
  installed on your computer. From your command line:
</p>
<pre><code
    ># Clone this repository
$ git clone https://github.com/baolhq/fcoder

# Go into the repository
$ cd fcoder

# Install dependencies
$ yarn

# Run the app
$ yarn dev
</code></pre>
<blockquote>
  <p>
    <strong>Note</strong>
    If you’re using Linux Bash for Windows,
    <a
      href="https://www.howtogeek.com/261575/how-to-run-graphical-linux-desktop-applications-from-windows-10s-bash-shell/"
      target="__blank">see this guide</a
    >
    or use <code>node</code> from the command prompt.
  </p>
</blockquote>
